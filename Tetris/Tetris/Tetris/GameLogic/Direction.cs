namespace Tetris.GameLogic
{
    /// <summary>
    /// Represents directions in which shape can move
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Indicates that shape will move to the left
        /// </summary>
        Left,
        /// <summary>
        /// Indicates that shape will move to the right
        /// </summary>
        Right,
        /// <summary>
        /// Indicates that shape will move down
        /// </summary>
        Bottom
    }
}
